function menu(){
    if(document.querySelector(".article .clanek").classList.contains("open")){
        document.querySelector(".article .clanek").classList.remove("open");
        document.querySelector(".article .clanek").classList.add("hide");
        document.querySelector(".fa-solid.icon1").classList.remove("fa-minus");
        document.querySelector(".fa-solid.icon1").classList.add("fa-plus");
    } else {
        document.querySelector(".article .clanek").classList.remove("hide");
        document.querySelector(".article .clanek").classList.add("open");
        document.querySelector(".fa-solid.icon1").classList.remove("fa-plus");
        document.querySelector(".fa-solid.icon1").classList.add("fa-minus");
    }
}
